    public static function index($limit,$pageCurr=1,$uid=0,$adplace,$fromTime=0,$toTime=0,$isuse=1,$isshow=2)
    {
        $apiUrl = ApiBase::getApiCurl() . '/api/v1/ad';
        $curl = new Curl();
        $curl->setHeader('X-Authorization', ApiBase::getApiKey());
        $curl->post($apiUrl, array(
            'limit' =>  $limit,
            'page'  =>  $pageCurr,
            'uid'       =>  $uid,
            'adplace'   =>  $adplace,
            'fromTime'  =>  $fromTime,
            'toTime'    =>  $toTime,
            'isuse'     =>  $isuse,
            'isshow'    =>  $isshow,
        ));
        $response = json_decode($curl->response);
        if ($response->error->code != 0) {
            return array('code' => -1, 'msg' => $response->error->msg);
        }
        return array(
            'code' => 0,
            'data' => ApiBase::objToArr($response->data),
            'pagelist'  =>  ApiBase::objToArr($response->pagelist),
            );
    }

